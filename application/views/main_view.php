	<h1>PFP <small>You are ready to roll!</small></h1>

	<p>PFP is a tiny application framework built for people who use a LAMP stack. PFP aims to be as simple as possible to set up and use.</p>

	<p>Visit <a href="http://remyg.fr/projects/pfp/">http://remyg.fr/projects/pfp/</a> for more information and documentation.</p>

	<h2>Requirements</h2>

	<ul>
		<li>PHP 5.1 or greater with support of PDO</li>
		<li>Drivers for any DBMS compatible with PDO (<a href="http://php.net/manual/en/pdo.drivers.php">list here</a>)</li>
		<li>The mod_rewrite Apache module</li>
	</ul>

	<h2>Installation</h2>

	<ul>
		<li>Download PFP and extract</li>
		<li>Navigate to <code>application/config/config.php</code> and fill in your <code>BASE_URL</code></li>
		<li>You are ready to rock! Point your browser to your <code>BASE_URL</code> and hopefully see a welcome message.</li>
	</ul>

